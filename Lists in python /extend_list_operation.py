#extend operation:adds an element at the end of a list
#appends function also do the same but it can add only one element but extend function can add  more then one element in the end of the list.
fruits=["mango","apple","banana"]
fruits.extend(["orange","pineapple","peach"])
print(fruits)

ceos=["satya nadela","sundar pichai"]
ceos.extend(["arvind krishna","tim cook"])
print(ceos)
